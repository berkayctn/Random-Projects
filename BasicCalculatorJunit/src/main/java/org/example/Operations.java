package org.example;

public class Operations {

    public int summation(int a, int b) {
        return a + b;
    }

    public int subtraction(int a, int b) {
        return a - b;
    }

    public int multiplication(int a, int b) {
        return a * b;
    }

    public double division(double a, int b) {
        return a / b;
    }
}
